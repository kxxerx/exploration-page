-- upgrade-v5.8-exploration-rooms.sql
-- 목적: 탐사 홈페이지 1차 실사용판용 방/참가자/상태/채팅 테이블과 RPC를 추가한다.
-- 전제: v5.3~v5.7 패치가 적용되어 profiles에 character_key/organization_code/department_code/affiliation_label이 존재한다.
-- 주의: service_role key, DB password, JWT secret은 프론트엔드/GitHub에 넣지 않는다.

create table if not exists public.exploration_rooms (
  id uuid primary key default gen_random_uuid(),
  scenario_id text not null,
  title text not null,
  host_user_id uuid not null references public.profiles(id) on delete cascade,
  invite_code text not null unique,
  max_players integer not null default 2 check (max_players between 1 and 4),
  status text not null default 'waiting' check (status in ('waiting', 'playing', 'ended')),
  created_at timestamptz not null default now(),
  started_at timestamptz,
  ended_at timestamptz
);

create table if not exists public.exploration_room_members (
  room_id uuid not null references public.exploration_rooms(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  role text not null default 'player' check (role in ('host', 'player', 'observer')),
  character_key_snapshot text,
  display_name_snapshot text not null default '익명',
  organization_code_snapshot text not null default 'unaffiliated',
  department_code_snapshot text not null default 'none',
  affiliation_label_snapshot text not null default '무소속',
  visitor_type_snapshot text not null default 'human',
  pollution_snapshot integer not null default 0,
  mask_collapse_rate_snapshot integer not null default 0,
  joined_at timestamptz not null default now(),
  left_at timestamptz,
  primary key (room_id, user_id),
  constraint exploration_room_members_org_check check (organization_code_snapshot in ('baekildream', 'disaster_agency', 'entity', 'unaffiliated', 'other')),
  constraint exploration_room_members_dept_check check (department_code_snapshot in ('field_exploration', 'research', 'security', 'agent', 'entity', 'none', 'other')),
  constraint exploration_room_members_visitor_check check (visitor_type_snapshot in ('human', 'infected', 'entity'))
);

create table if not exists public.exploration_room_state (
  room_id uuid primary key references public.exploration_rooms(id) on delete cascade,
  current_section_key text not null default 'intro',
  state_json jsonb not null default '{}'::jsonb,
  updated_by uuid references public.profiles(id) on delete set null,
  updated_at timestamptz not null default now()
);

create table if not exists public.exploration_room_messages (
  id bigint generated by default as identity primary key,
  room_id uuid not null references public.exploration_rooms(id) on delete cascade,
  sender_id uuid references public.profiles(id) on delete set null,
  sender_display_name text not null default '익명',
  message_type text not null default 'normal' check (message_type in ('normal', 'system', 'dice', 'item', 'whisper')),
  content text not null,
  created_at timestamptz not null default now()
);

create index if not exists exploration_rooms_host_idx on public.exploration_rooms(host_user_id, created_at desc);
create index if not exists exploration_rooms_invite_code_idx on public.exploration_rooms(invite_code);
create index if not exists exploration_room_members_user_idx on public.exploration_room_members(user_id, joined_at desc);
create index if not exists exploration_room_members_room_idx on public.exploration_room_members(room_id, joined_at);
create index if not exists exploration_room_messages_room_created_idx on public.exploration_room_messages(room_id, created_at);

alter table public.exploration_rooms enable row level security;
alter table public.exploration_room_members enable row level security;
alter table public.exploration_room_state enable row level security;
alter table public.exploration_room_messages enable row level security;

create or replace function public.is_exploration_room_member(p_room_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.exploration_room_members m
    where m.room_id = p_room_id
      and m.user_id = auth.uid()
      and m.left_at is null
  );
$$;

grant execute on function public.is_exploration_room_member(uuid) to authenticated;

create or replace function public.is_exploration_room_host(p_room_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.exploration_rooms r
    where r.id = p_room_id
      and r.host_user_id = auth.uid()
  );
$$;

grant execute on function public.is_exploration_room_host(uuid) to authenticated;

